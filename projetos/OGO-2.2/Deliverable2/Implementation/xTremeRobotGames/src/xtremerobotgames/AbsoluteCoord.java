package xtremerobotgames;

/**
 *
 * @author s102231
 */
public class AbsoluteCoord {

    private int x;
    private int y;

    AbsoluteCoord(int x1, int y1){
        x = x1;
        y = y1;
    }

    public int getX(){
        return x;
    }

    public int getY(){
        return y;
    }

}
