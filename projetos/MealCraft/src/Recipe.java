public class Recipe
{
    private String name;
    private String steps;
    private String notes;
    /**
       No-Arg constructor for recipe
    */
    public Recipe()
    {
    }
    public Recipe(String name, String steps)
    {
	this(name, steps, "");
    }
    public Recipe(String name, String steps, String notes)
    {
	this.name = name;
	this.steps=steps;
	this.notes=notes;
    }
    /**
       @return name
    */
    public String getName(){
	return name;
    }
    /**
       @return notes
    */
    public String getSteps(){
	return steps;
    }
    /**
       @return notes
    */
    public String getNotes(){
	return notes;
    }


}